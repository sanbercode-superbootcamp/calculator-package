var calculators = {
  plus: function(a, b) {
    return Number(a) + Number(b);
  },
  minus: function(a, b) {
    return Number(a) - Number(b);
  },
  times: function(a, b) {
    return Number(a) * Number(b);
  },
  divide: function(a, b) {
    return Number(a) / Number(b);
  }
}

module.exports = calculators;
