const calculator = {
    addition: function (num1, num2) {
        return num1 + num2;
    },
    subtraction: function (num1, num2) {
        return num1 - num2;
    },
    division: function (num1, num2) {
        return num1 / num2;
    },
    multiplication: function (num1, num2) {
        return num1 * num2;
    },
    modulus: function (num1, num2) {
        return num1 % num2;
    }
}


module.exports = calculator;