var calculator = {
    add : (a, b) => {
        return a+b
    },
    substract : (a, b) => {
        return a-b
    },
    multiple : (a, b) => {
        return a*b
    },
    division : (a, b) => {
        return a/b
    },
    exponen : (a, b) => {
        return a**b
    },
    modulus : (a, b) => {
        return a%b
    }

}

module.exports = calculator